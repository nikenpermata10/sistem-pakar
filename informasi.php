        <h2>Kanker Rahim</h2>
        <div class="image-box">
        <img src="assets/img/rahim.jpg" alt="Kanker Ovarium">
        </div>
        <p>
           Kanker rahim, juga dikenal sebagai kanker endometrium, terjadi di lapisan dalam rahim. Ini adalah kanker yang paling umum pada sistem reproduksi wanita di negara-negara berkembang        </p>
        <p>
            <strong>Gejala:</strong> Pendarahan vagina yang tidak normal, nyeri panggul, dan penurunan berat badan yang tidak dapat dijelaskan
        </p>
        <p>
            <strong>Pencegahan:</strong> 
            <li>Melaksanakan pola hidup sehat, makan-makanan bergizi, rajin berolahraga, rutin istirahat dengan cukup, dan lain sebagainya</li>
            <li>Tidak menggunakan obat-obatan tanpa aturan dari dokter, terlebih obat-obatan yang mampu mempengaruhi hormonal </li>
            <li> Rutin melaksanakan pemeriksaan ginekologi dan kesehatan lainya untuk memastikan keadaan tubuh</li>
        </p>
    </p>
    </div>

    <div class="content-section" id="kanker-serviks">
        <h2>Kanker Serviks</h2>
        <div class="image-box">
            <img src="assets/img/serviks.png" alt="Kanker Serviks">
        </div>
        <p>
        Kanker serviks adalah jenis kanker yang terjadi pada sel-sel di leher rahim. Ini adalah kanker kedua yang paling umum pada wanita di seluruh dunia. Human papillomavirus (HPV) adalah penyebab utama dari kanker serviks.        </p>
        <p>
            <strong>Gejala:</strong> Pendarahan vagina yang tidak normal, nyeri panggul, atau nyeri selama hubungan seksual
        </p>
        <p>
            <strong>Pencegahan:</strong>
            <li>Melakukan vaksinasi HPV agar tidak terinfeksi virusnya</li>
            <li> Melakukan pap tes secara rutin</li>
            <li> Berhenti merokok</li>
            <li>Tidak berganti-ganti pasangan</li>
        </p>
    </div>

    <div class="content-section" id="kanker-ovarium">
        <h2>Kanker Ovarium</h2>
        <div class="image-box">
            <img src="assets/img/ova.jpg" alt="Kanker Ovarium">
        </div>
        <p>
        Kanker ovarium dimulai di ovarium, dua organ kecil di sisi rahim yang menghasilkan telur. Kanker ini sering kali tidak terdeteksi sampai telah menyebar ke panggul dan perut, sehingga membuatnya lebih sulit untuk diobati        <p>
            <strong>Gejala:</strong> Perut kembung, cepat kenyang, nyeri perut, mual, sembelit, perut membengkak, penurunan berat badan, sering buang air kecil, sakit di punggung bagian bawah, nyeri saat berhungan seksual, dan pendarahan
        <p>
            <strong>Pencegahan:</strong>
            <li>Tidak menggunakan terapi pergantian hormon</li>
            <li>Menerapkan pola hidup sehat & Menjaga berat badan ideal</li>
            <li>Atur jarak kehamilan lebih dari 2 tahun </li>
        </p>
    </div>

    <div class="content-section" id="kanker-vulva">
        <h2>Kanker Vulva</h2>
        <div class="image-box">
            <img src="assets/img/vulva.jpg" alt="Kanker Serviks">
        </div>
        <p>
        Kanker vulva adalah jenis kanker yang terjadi di bagian luar alat kelamin wanita. Ini adalah jenis kanker yang jarang tetapi dapat diobati dengan baik jika ditemukan pada tahap awal  </p>     
             <strong>Gejala:</strong>nyeri, gatal pada daerah vulva, pendarahan, benjolan seperti kutil kecil, luka seperti borok pada daerah vulva
        <p>
            <strong>Pencegahan:</strong>
            <li>Pemeriksaan rutin dan menjaga kebersihan daerah genita</li>
            <li>Melakukan vaksin HPV</li>
            <li>Berhenti merokok</li>
        </p>
